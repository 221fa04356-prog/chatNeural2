export function getLangCode(storedLangString) {
    if (!storedLangString) return 'en';
    const displayName = storedLangString.split(',')[0].trim();
    return LANGUAGE_CODE_MAP[displayName] || 'en';
}

export function getTranslator(langCode) {
    const lang = translations[langCode] || translations['en'];
    const en = translations['en'];

    return function t(key, vars = {}) {
        const keys = key.split('.');

        let result = lang;
        for (const k of keys) {
            result = result?.[k];
            if (result === undefined) break;
        }

        if (result === undefined) {
            result = en;
            for (const k of keys) {
                result = result?.[k];
                if (result === undefined) break;
            }
        }

        if (typeof result !== 'string') return key;

        return result.replace(/\{(\w+)\}/g, (_, name) => vars[name] ?? `{${name}}`);
    };
}
